package activities;

// Activity 1: Printing the Results of Simple Arithmetic Operations
public class SimpleArithmetic {

    public static void main(String[] args) {
        System.out.println(String.format("The sum of %d + %d is %d", 3, 4, (3 + 4)));
        System.out.println(String.format("The product of %d + %d is %d", 3, 4, (3 * 4)));
    }
}
